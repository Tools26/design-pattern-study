package younggyun;

public class Shelve {
  void openFirstSlot() {
    System.out.println("선반의 첫번째 칸을 엽니다.");
  }

  void takeOutFirstSlot() { 
    System.out.println("선반의 첫번째 칸에서 물건을 뺍니다.");
  }

  void closeFirstSlot() {
    System.out.println("선반의 첫번째 칸을 닫습니다.");
  }

  void openSecondSlot() {
    System.out.println("선반의 두번째 칸을 엽니다.");
  }

  void takeOutSecondSlot() { 
    System.out.println("선반의 두번째 칸에서 물건을 뺍니다.");
  }
  
  void closeSecondSlot() {
    System.out.println("선반의 두번째 칸을 닫습니다.");
  }
}