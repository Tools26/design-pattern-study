package younggyun;

public class PowerStrip {
  void turnOnAll() {
    System.out.println("전체 전원을 킨다.");
  }

  void turnOnFirst() {
    System.out.println("첫번째 전원을 킨다.");
  }

  void turnOnSecond() {
    System.out.println("두번째 전원을 킨다.");
  }

  void turnOffAll() {
    System.out.println("전체 전원을 끈다.");
  }

  void turnOffFirst() {
    System.out.println("첫번째 전원을 끈다.");
  }

  void turnOffSecond() {
    System.out.println("두번째 전원을 끈다.");
  }
}